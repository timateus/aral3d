export const DISTRICT_MIGRATIONS: DistrictMigration[] = raw.map(([name, nameRu, vals]) => ({
  name,
  nameRu,
  values: Object.fromEntries(MIGRATION_YEARS.map((y, i) => [y, vals[i]])),
}));

/** Get the max migration value across all districts for a given year */
export function getMaxMigration(year: number): number {
  return Math.max(...DISTRICT_MIGRATIONS.map(d => d.values[year] ?? 0));
}

/** Get the total migration for a given year */
export function getTotalMigration(year: number): number {
  return DISTRICT_MIGRATIONS.reduce((sum, d) => sum + (d.values[year] ?? 0), 0);
}
